#in python è possibile gestire i cicli con la funzione range(). Qui troverete degli esempi: http://pythoncentral.io/pythons-range-function-explained/

#La funzione range prende un numero di partenza, uno di arrivo e un incremento.
#Per esempio se vogliamo stampare tutti i numeri da 0 a 10 incrementando ogni volta di 2 basterà scrivere:
for i in range(0,10,2):
    print(i)


